//Basic Math Functions
function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

console.log(add(7, 8));
console.log(subtract(7, 8));
console.log(multiply(7, 8));
console.log(divide(7, 8));
